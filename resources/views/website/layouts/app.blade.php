function getTripsData(){
    $.ajax({
        headers: {"X-CSRF-TOKEN": "{{ csrf_token() }}"},
        url: "{{ route('get_trips') }}",
        method: "POST",
        success: function(response){
            console.log(response)
            $('.sidebar-trips').html(response.html);
        }
    });
}

$(document.body).on('click', '.save-btn', function () {
            let tripId = $('#trip_id').val();
            let tripTitle = $('#trip_title').val()
            let locationId = $('#location_id').val();
            let locationName = $('#location_name').val();
            $.ajax({
                url: "{{ route('user.add-to-trip.detail') }}",
                method: "POST",
                data: {
                    _token: '{{ csrf_token() }}',
                    'trip_id': tripId,
                    'trip_title': tripTitle,
                    'location_id': locationId,
                    'location_name': locationName,
                    'location_from': $('#delivery_address').val(),
                    'location_img': $('#location_img').val()
                },
                success: function (response) {
                    if (response.status == 0) {
                        Swal.fire('წარმატებული!', response.msg, 'success');
                        $('#car_detail').modal('hide');
                        getTripsData()
                    }
                },
                error: function (xhr, ajaxOptions, thrownError) {
                    if (xhr.status == 422) { // when status code is 422, it's a validation issue
                        $('.errors').html('');
                        $.each(xhr.responseJSON.errors, function (i, error) {
                            $('.' + i + '_err').html(error);
                        });
                    }
                    $this.html('შენახვა');
                    $this.prop('disabled', false);
                }
            })
        })
</script>
